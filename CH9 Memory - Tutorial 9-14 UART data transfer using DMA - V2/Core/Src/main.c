void DMATransferComplete(DMA_HandleTypeDef* hdma_usart1_tx){
	/*Disable UART DMA mode */
	huart1.Instance->CR3 &= ~USART_CR3_DMAT;
	/*Toggle Green Led*/
	HAL_GPIO_TogglePin(GPIOG, GPIO_PIN_13);
}
